package ast;

public class DecConValorIni {
	private boolean conValorIni;
	private E valorIni;
	
	public DecConValorIni(boolean conValorIni, E valorIni) {
		super();
		this.conValorIni = conValorIni;
		this.valorIni = valorIni;
	}

	public boolean isConValorIni() {
		return conValorIni;
	}

	public E getValorIni() {
		return valorIni;
	}
	
	
}
